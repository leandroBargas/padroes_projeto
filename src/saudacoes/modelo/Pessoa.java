package saudacoes.modelo;

public class Pessoa {
	
	public String nome;
	public String sexo;

	public Pessoa() {
		super();
		
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getSexo() {
		return sexo;
	}

	public void setSexo(String sexo) {
		this.sexo = sexo;
	}



}
